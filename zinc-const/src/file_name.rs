//!
//! The Zinc source file names.
//!

/// The project manifest file name.
pub static MANIFEST: &str = "Zargo";

/// The circuit or contract entry file name.
pub static APPLICATION_ENTRY: &str = "main";

/// The library entry file name.
pub static LIBRARY_ENTRY: &str = "lib";

/// The source code complex module default name.
pub static MODULE_ENTRY: &str = "mod";

/// The binary file default name.
pub static BINARY: &str = "main";

/// The input template file default name.
pub static INPUT: &str = "input";

/// The output template file default name.
pub static OUTPUT: &str = "output";

/// The proving key file default name.
pub static PROVING_KEY: &str = "proving_key";

/// The verifying key file default name.
pub static VERIFYING_KEY: &str = "verifying_key";

/// The private key file default name (testnet only!).
pub static PRIVATE_KEY: &str = "private_key";

/// The integration test scenario file default name.
pub static SCENARIO: &str = "scenario";
